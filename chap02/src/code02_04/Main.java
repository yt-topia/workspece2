package code02_04;

public class Main {

	public static void main(String[] args) {
		int a;
		a = 100;
		a++;
		System.out.println(a);
		
		int b = 100;
		++b;
		System.out.println(b);
		
		
		int c = 10;
		int d = 10;
		System.out.println(50 + ++c);
		System.out.println(50 + d++);
		System.out.println(d);

	}

}
